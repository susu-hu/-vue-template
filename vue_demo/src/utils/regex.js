// ----邮政编码---/^[1-9]\d{5}$/
// ----注册账号和-----/^[a-zA-Z][a-zA-Z0-9_]{4,15}$/
export default {
    isPhone(e) {
        var reg_tel = /^(13[0-9]|14[01456879]|15[0-35-9]|16[2567]|17[0-8]|18[0-9]|19[0-35-9])\d{8}$/;
        let flag = false;
        if (reg_tel.test(e)) {
            flag = true
        }
        return flag
    }
}